export function validate(
  req: Request,
  res: Response,
  next: NextFunction
): void {
  const errors = validationResult(req);
  
  if (!errors.isEmpty()) {
    const formattedErrors = errors.array().map((error: any) => ({
      field: error.path,
      message: error.msg,
    }));
    
    next(createError(400, 'Validation failed', { details: formattedErrors }));
    return;
  }
  
  next();
}
